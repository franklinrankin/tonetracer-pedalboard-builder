export function getPedalsByCategory(category: string): Pedal[] {
  return PEDALS.filter(p => p.category === category);
}

export function searchPedals(query: string): Pedal[] {
  const lower = query.toLowerCase();
  return PEDALS.filter(p => 
    p.brand.toLowerCase().includes(lower) ||
    p.model.toLowerCase().includes(lower) ||
    p.category.toLowerCase().includes(lower) ||
    p.subtype?.toLowerCase().includes(lower)
  );
}

